'''O usuário irá digitar a sua idade e, caso a idade seja maior ou igual a 18, terá como resposta “Maior de Idade”. Caso contrário, “Menor de Idade”.'''

MaiorIdade = 18

idade = int(input("Digite a sua idade:\n"))

if (idade >= MaiorIdade):
    print("Maior de Idade")
else:
        print("Menor de Idade")
        
